    private void documentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_documentoActionPerformed
        if (((String) documento.getSelectedItem()).equals("Libro")) {
            volumen.setEnabled(false);
            numero.setEnabled(false);
            mes.setEnabled(false);
            prestamo1.setEnabled(true);
            prestamo2.setEnabled(true);
        } else if (((String) documento.getSelectedItem()).equals("Revista")) {
            prestamo1.setEnabled(false);
            prestamo2.setEnabled(false);
            volumen.setEnabled(true);
            numero.setEnabled(true);
            mes.setEnabled(true);
        } else {
            prestamo1.setEnabled(false);
            prestamo2.setEnabled(false);
            volumen.setEnabled(false);
            numero.setEnabled(false);
            mes.setEnabled(false);
        }
    }//GEN-LAST:event_documentoActionPerformed

