    private void ingresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ingresarActionPerformed
        String ti = titulo.getText();
        String au = autor.getText();
        String edi = editorial.getText();
        int codi = Integer.parseInt(codigo.getText());
        String añ = año.getText();
        boolean prestamo;

        if (((String) documento.getSelectedItem()).equals("Libro")) {
            if (prestamo1.isSelected()) {
                prestamo = true;
            } else {
                prestamo = false;
            }
            Libro l = new Libro(prestamo, codi, ti, au, edi, añ);
            RegistrarDocumentos.agregarDocumento(l);
        }
        if (((String) documento.getSelectedItem()).equals("Revista")) {
            int vol = Integer.parseInt(volumen.getText());
            int num = Integer.parseInt(numero.getText());
            String me = String.valueOf(mes.getSelectedItem());
            Revista r = new Revista(vol, num, me, codi, ti, au, edi, añ);
            RegistrarDocumentos.agregarDocumento(r);
        }

    }//GEN-LAST:event_ingresarActionPerformed

